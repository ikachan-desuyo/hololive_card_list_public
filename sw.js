// Install event
self.addEventListener('install', function(event) {
  console.log('%c‚ö° Service Worker: Install Event', 'color: #4CAF50; font-weight: bold;');
  logVersionInfo();
  
  // Âº∑Âà∂ÁöÑ„Å´Âç≥Â∫ß„Å´„Çπ„Ç≠„ÉÉ„ÉóÂæÖÊ©ü
  self.skipWaiting();
  
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(function(cache) {
        console.log('%cüì¶ Service Worker: Caching files...', 'color: #2196F3;');
        return cache.addAll(urlsToCache);
      })
      .then(function() {
        console.log('%c‚úÖ Service Worker: All files cached successfully', 'color: #4CAF50;');
        return self.skipWaiting();
      })
      .catch(function(error) {
        console.error('‚ùå Service Worker: Caching failed:', error);
      })
  );
});

// Activate event
self.addEventListener('activate', function(event) {
  console.log('%cüîÑ Service Worker: Activate Event', 'color: #FF9800; font-weight: bold;');
  
  event.waitUntil(
    Promise.all([
      // Delete old caches and create fresh cache
      caches.keys().then(function(cacheNames) {
        console.log('Found caches:', cacheNames);
        return Promise.all(
          cacheNames.map(function(cacheName) {
            if (cacheName !== CACHE_NAME) {
              console.log('%cüóëÔ∏è Service Worker: Deleting old cache:', cacheName, 'color: #F44336;');
              return caches.delete(cacheName);
            }
          })
        );
      }).then(function() {
        // Recreate cache with fresh content for HTML files
        return caches.open(CACHE_NAME).then(cache => {
          console.log('Updating cache with fresh HTML content:', CACHE_NAME);
          const cacheBustingUrls = urlsToCache.map(url => {
            if (url.endsWith('.html') || url === './') {
              return `${url}?v=${APP_VERSION}&t=${Date.now()}`;
            }
            return url;
          });
          return cache.addAll(cacheBustingUrls);
        });
      }),
      // Immediately claim all clients
      self.clients.claim().then(function() {
        console.log('%c‚úÖ Service Worker: Activated and claimed clients', 'color: #4CAF50;');
        // Notify all clients about cache update
        return self.clients.matchAll().then(clients => {
          clients.forEach(client => {
            client.postMessage({
              type: 'CACHE_UPDATED',
              message: 'Service Worker updated with modular structure',
              version: APP_VERSION,
              timestamp: Date.now()
            });
          });
        });
      })
    ])
  );
});

// Fetch event - Network First for HTML, Cache First for other resources
self.addEventListener('fetch', function(event) {
  // Skip non-GET requests
  if (event.request.method !== 'GET') {
    return;
  }

  // Â§ñÈÉ®ÁîªÂÉèURLÔºàhololive-official-cardgame.comÔºâ„ÇÇÂá¶ÁêÜÂØæË±°„Å´„Åô„Çã
  const isExternalImage = event.request.url.includes('hololive-official-cardgame.com') &&
                          (event.request.url.includes('.jpg') ||
                           event.request.url.includes('.png') ||
                           event.request.url.includes('.jpeg') ||
                           event.request.url.includes('.webp'));

  // ÂÜÖÈÉ®„É™„ÇΩ„Éº„Çπ„Åæ„Åü„ÅØÂ§ñÈÉ®ÁîªÂÉèURL„ÅÆÂ†¥Âêà„ÅÆ„ÅøÂá¶ÁêÜ
  if (!event.request.url.startsWith(self.location.origin) && !isExternalImage) {
    return;
  }

  // HTML„Éï„Ç°„Ç§„É´„Å´ÂØæ„Åó„Å¶„ÅØNetwork FirstÊà¶Áï•„Çí‰ΩøÁî®
  const isHTMLFile = event.request.url.endsWith('.html') || 
                     event.request.url === self.location.origin + '/' ||
                     event.request.url.endsWith('/');

  if (isExternalImage) {
    // Â§ñÈÉ®ÁîªÂÉè„ÅÆÂ†¥Âêà„ÅØÁâπÂà•„Å™Âá¶ÁêÜ
    console.log('üñºÔ∏è Service Worker: Handling external image:', event.request.url);
    
    event.respondWith(
      caches.match(event.request)
        .then(function(response) {
          if (response) {
            console.log('Serving cached image:', event.request.url);
            return response;
          }
          
          // „Ç≠„É£„ÉÉ„Ç∑„É•„Å´„Å™„Åë„Çå„Å∞„Éç„ÉÉ„Éà„ÉØ„Éº„ÇØ„Åã„ÇâÂèñÂæóÔºàno-cors„É¢„Éº„ÉâÔºâ
          return fetch(event.request, { mode: 'no-cors' }).then(function(response) {
            if (response && response.type === 'opaque') {
              // opaque„É¨„Çπ„Éù„É≥„Çπ„Çí„Ç≠„É£„ÉÉ„Ç∑„É•„Å´‰øùÂ≠ò
              const responseToCache = response.clone();
              caches.open(CACHE_NAME).then(function(cache) {
                cache.put(event.request, responseToCache).then(() => {
                  console.log('üñºÔ∏è Successfully cached external image:', event.request.url);
                }).catch((error) => {
                  console.warn('‚ö†Ô∏è Failed to cache external image:', event.request.url, error);
                });
              });
            }
            return response;
          }).catch(function() {
            // „Éç„ÉÉ„Éà„ÉØ„Éº„ÇØÂ§±ÊïóÊôÇ„ÅØ„Éó„É¨„Éº„Çπ„Éõ„É´„ÉÄ„Éº„ÇíÊèê‰æõ
            console.log('Network failed for external image, using placeholder:', event.request.url);
            return caches.match('./images/placeholder.png');
          });
        })
    );
  } else if (isHTMLFile) {
    // „É≠„Ç∞„ÇíÊ∏õ„Çâ„Åô„Åü„ÇÅ„Å´„ÄÅHTML„Éï„Ç°„Ç§„É´„ÅÆ„É™„ÇØ„Ç®„Çπ„Éà„ÅÆ„Åø„É≠„Ç∞Âá∫Âäõ
    console.log('%cüåê Service Worker: Fetching HTML with Network First', event.request.url, 'color: #607D8B;');
    
    event.respondWith(
      // Network First: „Åæ„Åö„Éç„ÉÉ„Éà„ÉØ„Éº„ÇØ„Åã„ÇâÂèñÂæó„ÇíË©¶Ë°åÔºà„Ç≠„É£„ÉÉ„Ç∑„É•„Éê„Çπ„ÉÜ„Ç£„É≥„Ç∞‰ªò„ÅçÔºâ
      fetch(event.request.url + (event.request.url.includes('?') ? '&' : '?') + 't=' + Date.now(), {
        cache: 'no-cache',
        headers: {
          'Cache-Control': 'no-cache, no-store, must-revalidate',
          'Pragma': 'no-cache',
          'Expires': '0'
        }
      })
        .then(function(response) {
          if (response && response.status === 200) {
            // „Éç„ÉÉ„Éà„ÉØ„Éº„ÇØ„Åã„ÇâÂèñÂæóÊàêÂäüÊôÇ„ÅØ„Ç≠„É£„ÉÉ„Ç∑„É•„ÇíÊõ¥Êñ∞
            const responseToCache = response.clone();
            caches.open(CACHE_NAME).then(function(cache) {
              cache.put(event.request, responseToCache);
            });
            console.log('Serving fresh HTML from network:', event.request.url);
            return response;
          }
          throw new Error('Network response not ok');
        })
        .catch(function() {
          // „Éç„ÉÉ„Éà„ÉØ„Éº„ÇØÂ§±ÊïóÊôÇ„ÅØ„Ç≠„É£„ÉÉ„Ç∑„É•„Åã„ÇâÊèê‰æõ
          console.log('Network failed, serving HTML from cache:', event.request.url);
          return caches.match(event.request);
        })
    );
  } else {
    // „Åù„ÅÆ‰ªñ„ÅÆ„É™„ÇΩ„Éº„Çπ„ÅØCache FirstÊà¶Áï•
    event.respondWith(
      caches.match(event.request)
        .then(function(response) {
          // „Ç≠„É£„ÉÉ„Ç∑„É•„Å´„ÅÇ„Çå„Å∞„Åù„Çå„ÇíËøî„Åô
          if (response) {
            return response;
          }
          
          // „Ç≠„É£„ÉÉ„Ç∑„É•„Å´„Å™„Åë„Çå„Å∞„Éç„ÉÉ„Éà„ÉØ„Éº„ÇØ„Åã„ÇâÂèñÂæó
          return fetch(event.request).then(function(response) {
            // ÊúâÂäπ„Å™„É¨„Çπ„Éù„É≥„Çπ„Åã„ÉÅ„Çß„ÉÉ„ÇØ
            if (!response || response.status !== 200 || response.type !== 'basic') {
              return response;
            }
            
            // „É¨„Çπ„Éù„É≥„Çπ„Çí„ÇØ„É≠„Éº„É≥„Åó„Å¶„Ç≠„É£„ÉÉ„Ç∑„É•„Å´‰øùÂ≠ò
            const responseToCache = response.clone();
            
            caches.open(CACHE_NAME)
              .then(function(cache) {
                // „Ç´„Éº„ÉâÁîªÂÉè„ÇíÂãïÁöÑ„Å´„Ç≠„É£„ÉÉ„Ç∑„É•ÔºàÂÑ™ÂÖàÂ∫¶„Çí‰∏ä„Åí„Çã„Éª„É≠„Ç∞ËøΩÂä†Ôºâ
                if (event.request.url.includes('hololive-official-cardgame.com') ||
                    event.request.url.includes('.jpg') ||
                    event.request.url.includes('.png') ||
                    event.request.url.includes('.jpeg') ||
                    event.request.url.includes('.webp')) {
                  cache.put(event.request, responseToCache).then(() => {
                    console.log('üñºÔ∏è Successfully cached image:', event.request.url);
                  }).catch((error) => {
                    console.warn('‚ö†Ô∏è Failed to cache image:', event.request.url, error);
                  });
                }
              });
            
            return response;
          }).catch(function() {
            // „Éç„ÉÉ„Éà„ÉØ„Éº„ÇØÂ§±ÊïóÊôÇ„ÄÅÁîªÂÉè„ÅÆÂ†¥Âêà„ÅØ„Éó„É¨„Éº„Çπ„Éõ„É´„ÉÄ„Éº„ÇíÊèê‰æõ
            if (event.request.destination === 'image') {
              return caches.match('./images/placeholder.png');
            }
          });
        })
        .catch(function(error) {
          console.error('‚ùå Service Worker: Fetch failed:', error);
          
          // HTML„Éö„Éº„Ç∏„ÅÆÂ†¥Âêà„ÅØ„Ç™„Éï„É©„Ç§„É≥Áî®„ÅÆ„Éï„Ç©„Éº„É´„Éê„ÉÉ„ÇØ
          if (event.request.destination === 'document') {
            return caches.match('./index.html');
          }
        })
    );
  }
});

